<?php
    // функция которая возвращает сумму элементов массива

    function custom_arr_sum($arr){

        // объявим счетчек и присвоим ему значение 0
        $i = 0;

        // переберем массив с помощью цикла foreach

        foreach ($arr as $item){
            $i += $item; // $i = $i + $item
        }

        // возвращаем счетчик $i поскольку в нем находится сумма элементов массива.

        return $i;
    }

