     */


    /*
      написать функцию которая если:
        $num1 == $num2, вернет сумму ($num1 + $num2)
        $num1 < $num2, вернем "Результат отрицательный, т. к. $num1 < $num2"
        $num1 != $num2, вернет разность ($num1 - $num2)
     */

    function check_params($num1, $num2){
        if ($num1 == $num2){
            $res = $num1 + $num2;

            return $res;
        } elseif ($num1 < $num2){
            $res = "Результат отрицательный, т. к. $num1 < $num2";

            return $res;
        } else {
            $res = $num1 - $num2;
            return $res;
        }
    }

    // тестирование функции
    $test1 = check_params(5,5); // 10
    $test2 = check_params(2,6); // Результат отрицательный, т. к. 2 < 6
    $test3 = check_params(6,1); // 5

