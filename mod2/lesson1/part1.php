        Функция объявляется с помощью ключевого слова function,
        далее указывается имя функции, затем идут () в которые
        подставляются аргументы функции при необходимости
        далее фигурные скобки в которых реализуется код (тело функции)

        return - возвращает значение
        после выполнения return, код расположенный в фигурных скобках, не выполняется


        Синтаксис:
        function имя_функции(){
            тело функции (код реализуемый в функции)
        }
     */

    // функция принимающая 2 параметра
    function S($a, $b){
        $res = $a * $b;

        return $res;
    }

    // тестирование функции
    $test1 = S(2, 4); // 8
